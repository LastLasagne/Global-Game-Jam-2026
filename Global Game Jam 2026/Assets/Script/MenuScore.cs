using UnityEngine;

public class MenuScore : MonoBehaviour
{
    public static MenuScore Instance;
    public int score = 0;

    private void Awake()
    {
        if (Instance == null)
        {
            Instance = this;
            DontDestroyOnLoad(gameObject);
        }
        else
        {
            Destroy(gameObject);
        }
    }
}
